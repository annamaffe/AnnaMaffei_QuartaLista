public class AnnaMaffei_Exercicio5{
    public static void main(String[] args) {

        //inicialização do for com a criação da variavel "i"
        for (int i = 10; i >= 0; i--) { // "--" para programar para menos que 10
            System.out.println(i);
        }
        
        // mostra "fim" ao chegar no 0
        System.out.println("FIM");
    }
}


